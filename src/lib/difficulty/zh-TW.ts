function getLevel(word: string): CEFRLevel {
  if (HSK1.has(word)) return "A1";
  if (HSK2.has(word)) return "A2";
  if (HSK3.has(word)) return "B1";
  if (HSK4.has(word)) return "B2";
  if (HSK5.has(word)) return "C1";
  if (HSK6.has(word)) return "C2";
  return "C1"; // default for unknown
}

